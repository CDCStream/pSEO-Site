export function calculateCropValue({ basePrice, quality, selectedMutations, friendBoost, amount }) {
  const qualityMultiplier = quality.multiplier;
  const mutationSum = selectedMutations.reduce((sum, m) => sum + m.multiplier, 0);
  const mutationCount = selectedMutations.length;
  const mutationFactor = mutationCount > 0 ? (1 + mutationSum - mutationCount) : 1;
  const friendFactor = 1 + (friendBoost / 100);
  const totalMultiplier = qualityMultiplier * mutationFactor * friendFactor;
  const finalValue = basePrice * totalMultiplier * amount;

  return {
    qualityMultiplier,
    mutationFactor,
    friendFactor,
    totalMultiplier,
    finalValue,
    amount,
  };
}

export function formatValue(value) {
  if (value >= 1e15) return `$${(value / 1e15).toFixed(2)}Q`;
  if (value >= 1e12) return `$${(value / 1e12).toFixed(2)}T`;
  if (value >= 1e9) return `$${(value / 1e9).toFixed(2)}B`;
  if (value >= 1e6) return `$${(value / 1e6).toFixed(2)}M`;
  if (value >= 1e3) return `$${(value / 1e3).toFixed(2)}K`;
  return `$${value.toFixed(2)}`;
}
